package example01;

import javax.swing.JOptionPane;

public class Operating01 {

	public static void main(String[] args) {

		int aVar=20;

	 System.out.println("aVar+=10 > "+(aVar+=10));
	 System.out.println("aVar-=10 > "+(aVar-=10));
	 System.out.println("aVar*=10 > "+(aVar*=10));
	 System.out.println("aVar/=10 > "+(aVar/=10));
	 System.out.println("aVar%=10 > "+(aVar%=10));
	}

}
